import React, { useRef } from 'react'
import { motion } from 'framer-motion'

// PostHog Style Post Card - Always expanded with clean design
export default function PostCard({ post, onClick, isActive }) {
  const { title, excerpt, category, date, author } = post
  const cardRef = useRef(null)

  const handleClick = (e) => {
    e.preventDefault()
    onClick(post)
  }

  return (
    <motion.article
      ref={cardRef}
      className={`post-card-expanded ${isActive ? 'post-card-active' : ''}`}
      onClick={handleClick}
      initial={{ opacity: 0, y: 10 }}
      animate={{ opacity: 1, y: 0 }}
      whileHover={{ y: -2 }}
      whileTap={{ scale: 0.995 }}
      transition={{ duration: 0.2, ease: 'easeOut' }}
    >
      {/* Header with Icon and Title */}
      <div className="post-card-header">
        <span className="post-card-icon">
          {getCategoryIcon(category)}
        </span>
        <div className="post-card-title-wrap">
          <h3 className="post-card-title">{title}</h3>
          <div className="post-card-meta">
            {category && <span className="post-card-category">{category}</span>}
            {date && <span className="post-card-date">{date}</span>}
          </div>
        </div>
        {/* Open indicator */}
        <div className="post-card-open-indicator">
          <svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="1.5">
            <path strokeLinecap="round" strokeLinejoin="round" d="M13.5 6H5.25A2.25 2.25 0 0 0 3 8.25v10.5A2.25 2.25 0 0 0 5.25 21h10.5A2.25 2.25 0 0 0 18 18.75V10.5m-10.5 6L21 3m0 0h-5.25M21 3v5.25" />
          </svg>
        </div>
      </div>

      {/* Content - Always visible */}
      <div className="post-card-body">
        <p className="post-card-excerpt">{excerpt}</p>

        {/* Author */}
        {author && (
          <div className="post-card-author">
            <span className="post-card-author-avatar">
              {author.avatar ? (
                <img src={author.avatar} alt={author.name} loading="lazy" decoding="async" />
              ) : (
                (author.name?.[0] || 'A').toUpperCase()
              )}
            </span>
            <span className="post-card-author-name">{author.name}</span>
          </div>
        )}
      </div>

      {/* Featured Image if available */}
      {post.featuredImage && (
        <div className="post-card-image">
          <img src={post.featuredImage} alt={title} loading="lazy" decoding="async" />
        </div>
      )}
    </motion.article>
  )
}

// Container for cards
export function MorphingDisclosure({ children, className = '' }) {
  return (
    <div className={`morphing-disclosure ${className}`}>
      {children}
    </div>
  )
}


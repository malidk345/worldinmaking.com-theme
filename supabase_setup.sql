-- Blog Yazıları (Posts)
create table if not exists public.posts (
  id bigint generated by default as identity primary key,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  title text not null,
  content text not null,
  excerpt text,
  slug text,
  author text not null,
  author_avatar text,
  category text default 'uncategorized',
  published boolean default true
);

alter table public.posts enable row level security;
drop policy if exists "Herkes yazıları görebilir" on public.posts;
create policy "Herkes yazıları görebilir" on public.posts for select using ( published = true );

drop policy if exists "Sadece adminler yazı yönetebilir" on public.posts;
create policy "Sadece adminler yazı yönetebilir" on public.posts for all using ( 
  exists ( select 1 from public.profiles where id = auth.uid() and role = 'admin' ) 
);


-- Yorumlar (Comments)
create table if not exists public.comments (
  id bigint generated by default as identity primary key,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  content text not null,
  post_id text not null,
  user_id uuid references public.profiles(id) not null,
  parent_id bigint references public.comments(id)
);

alter table public.comments enable row level security;
drop policy if exists "Herkes yorumları görebilir" on public.comments;
create policy "Herkes yorumları görebilir" on public.comments for select using (true);

drop policy if exists "Giriş yapmış kullanıcı yorum atabilir" on public.comments;
create policy "Giriş yapmış kullanıcı yorum atabilir" on public.comments for insert with check (auth.role() = 'authenticated');

drop policy if exists "Admin yorum silebilir" on public.comments;
create policy "Admin yorum silebilir" on public.comments for delete using ( 
    exists ( select 1 from public.profiles where id = auth.uid() and role = 'admin' ) 
);


-- Beğeniler (Likes)
create table if not exists public.post_likes (
  id bigint generated by default as identity primary key,
  user_id uuid references auth.users not null,
  post_id text not null,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  unique(user_id, post_id)
);

alter table public.post_likes enable row level security;
drop policy if exists "Herkes beğenileri görebilir" on public.post_likes;
create policy "Herkes beğenileri görebilir" on public.post_likes for select using (true);

drop policy if exists "Üyeler beğeni atabilir" on public.post_likes;
create policy "Üyeler beğeni atabilir" on public.post_likes for insert with check (auth.uid() = user_id);

drop policy if exists "Üyeler beğenisini geri alabilir" on public.post_likes;
create policy "Üyeler beğenisini geri alabilir" on public.post_likes for delete using (auth.uid() = user_id);



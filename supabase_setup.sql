-- Profil Tablosu
create table if not exists public.profiles (
  id uuid references auth.users not null primary key,
  updated_at timestamp with time zone,
  username text unique,
  avatar_url text,
  website text,
  role text default 'user' -- 'admin' veya 'user'
);

-- RLS (Güvenlik) - Profiles
alter table public.profiles enable row level security;
drop policy if exists "Public profiles" on public.profiles;
create policy "Public profiles" on public.profiles for select using (true);

drop policy if exists "Self insert" on public.profiles;
create policy "Self insert" on public.profiles for insert with check (auth.uid() = id);

drop policy if exists "Self update" on public.profiles;
create policy "Self update" on public.profiles for update using (auth.uid() = id);

-- Constraint Temizliği (Kısa emaillerin sorun çıkarmaması için)
alter table public.profiles drop constraint if exists username_length;

-- Profil Oluşturma Trigger (Güvenli ve Hatasız Kayıt İçin)
create or replace function public.handle_new_user() 
returns trigger as $$
declare
  raw_username text;
  final_username text;
begin
  -- Email veya Metadata'dan Username Belirle
  raw_username := new.raw_user_meta_data->>'username';
  if raw_username is null or length(raw_username) = 0 then
    raw_username := split_part(new.email, '@', 1);
  end if;
  
  -- Çok kısaysa ID kullan
  if raw_username is null or length(raw_username) < 2 then
     final_username := 'user_' || substr(new.id::text, 1, 8);
  else
     final_username := raw_username;
  end if;

  -- Kayıt oluştur (Çakışma durumunda da çalışır)
  begin
    insert into public.profiles (id, username, avatar_url, role)
    values (
      new.id, 
      final_username, 
      'https://i.pravatar.cc/150?u=' || new.id,
      'user'
    );
  exception when unique_violation then
    insert into public.profiles (id, username, avatar_url, role)
    values (
      new.id, 
      final_username || '_' || substr(md5(random()::text), 1, 4), 
      'https://i.pravatar.cc/150?u=' || new.id,
      'user'
    );
  end;
  return new;
end;
$$ language plpgsql security definer;

-- Trigger Bağlantısı
drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created
  after insert on auth.users
  for each row execute procedure public.handle_new_user();


